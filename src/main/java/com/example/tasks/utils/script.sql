CREATE OR REPLACE VIEW active_tasks_v AS
SELECT task_id, task_name, due_date
FROM tasks T
         JOIN status_types S ON T.STATUS_TYPE_ID = S.STATUS_TYPE_ID
WHERE status_name IN ('In Progress', 'Not Started');


CREATE OR REPLACE FUNCTION get_task_status(task_id_input NUMBER)
RETURN VARCHAR2AS task_status_output VARCHAR2(255);
BEGIN SELECT ST.status_name INTO task_status_output
      FROM status_typesST JOIN tasks T ON ST.STATUS_TYPE_ID = T.STATUS_TYPE_ID
      WHERE T.task_id= task_id_input;
RETURN task_status_output;
END;



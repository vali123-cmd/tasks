

CREATE TABLE status_types(
                             status_type_id VARCHAR2(255),
                             status_name VARCHAR2(255),
                             creation_date                DATE default SYSDATE not null,
                             created_by                   VARCHAR2(50) default USER not null,
                             last_update_date             DATE default SYSDATE NOT NULL,
                             last_updated_by              VARCHAR2(50) default USER not null,
                             created_by_fullname          VARCHAR2(300),
                             CONSTRAINT status_types_pk PRIMARY KEY(status_type_id)
);



CREATE TABLE users(
                      user_id NUMBER(13) GENERATED BY DEFAULT AS IDENTITY,
                      username VARCHAR2(255),
                      birth_date DATE,
                      is_internal NUMBER(1) DEFAULT 1 NOT NULL,
                      creation_date                DATE default SYSDATE not null,
                      created_by                   VARCHAR2(50) default USER not null,
                      last_update_date             DATE default SYSDATE NOT NULL,
                      last_updated_by              VARCHAR2(50) default USER not null,
                      created_by_fullname          VARCHAR2(300),
                      CONSTRAINT users_pk PRIMARY KEY(user_id)

);





CREATE TABLE tasks(
                      task_id NUMBER(13) GENERATED BY DEFAULT AS IDENTITY,
                      name VARCHAR2(500) NOT NULL,
                      status_type_id VARCHAR2(255),
                      user_id NUMBER(13),
                      due_date DATE,
                      created_by VARCHAR2(50) DEFAULT USER,

                      CONSTRAINT tasks_pk PRIMARY KEY(task_id),
                      CONSTRAINT task_statuses_fk FOREIGN KEY(status_type_id) REFERENCES status_types(status_type_id),
                      CONSTRAINT task_users_fk FOREIGN KEY(user_id) REFERENCES users(user_id)
);



CREATE OR REPLACE PROCEDURE sterge_cancelled AS
BEGIN
DELETE FROM tasks
WHERE status_type_id = (
    SELECT status_type_id
    FROM status_types
    WHERE status_name = 'Cancelled'
)
  AND SYSDATE - due_date >= 3;

COMMIT;
END;



